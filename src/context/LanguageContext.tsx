export interface LanguageContextType {
  languageCode: string;
  setLanguageCode: (code: string) => void;
  language: Language;
  t: (key: TranslationKey) => string;
  showSelector: boolean;
  openSelector: () => void;
  closeSelector: () => void;
  isRTL: boolean;
}

const LanguageContext = createContext<LanguageContextType | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [languageCode, setCodeState] = useState<string>(
    () => localStorage.getItem("reliefhub_lang") ?? ""
  );
  const [showSelector, setShowSelector] = useState<boolean>(false);

  function setLanguageCode(code: string) {
    localStorage.setItem("reliefhub_lang", code);
    setCodeState(code);
  }

  const effectiveCode = languageCode || "en";
  const language = LANGUAGES.find((l) => l.code === effectiveCode) ?? {
    code: "en", english: "English", native: "English", popular: false,
  };
  const isRTL = effectiveCode === "ur" || effectiveCode === "ks" || effectiveCode === "sd";

  const t = (key: TranslationKey): string => {
    const lang = T[effectiveCode] ?? {};
    return lang[key] ?? T["en"]?.[key] ?? (key as string);
  };

  return (
    <LanguageContext.Provider value={{
      languageCode: effectiveCode,
      setLanguageCode,
      language,
      t,
      showSelector,
      openSelector: () => setShowSelector(true),
      closeSelector: () => setShowSelector(false),
      isRTL,
    }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used within LanguageProvider");
  return ctx;
}
